// ── 날짜 컨텍스트 (현재 시각 기준 자동 계산) ──────────────────
function buildDateContext(): string {
  const now = new Date();
  const y = now.getFullYear();
  const m = now.getMonth() + 1;
  const d = now.getDate();
  const season =
    m <= 2 || m === 12 ? '겨울(FW)' : m <= 5 ? '봄(SS)' : m <= 8 ? '여름(SS)' : '가을(FW)';
  return [
    `- 오늘: ${y}-${String(m).padStart(2, '0')}-${String(d).padStart(2, '0')} (올해=${y}년, 현재 시즌≈${season})`,
    `- "전년 동기간"=${y - 1}년 같은 월/주, "전년 대비/YoY"=${y}년 vs ${y - 1}년, "재작년"=${y - 2}년.`,
    `- 연도를 언급할 땐 반드시 위 기준을 따르고, ${y}년을 현재로 간주한다. 과거 학습 시점의 연도를 쓰지 말 것.`,
  ].join('\n');
}

