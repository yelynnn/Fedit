// 패션 기획자/디자이너가 사용하는 표준 분류 체계.
// 에이전트는 스타일·소재·패턴·컬러를 묘사/태깅할 때 반드시 아래 표준 용어를 사용한다.
// 원본 목록을 그대로 보존하며, 런타임에 배열/객체로 파싱한다.

function lines(raw: string): string[] {
  return raw
    .split('\n')
    .map((s) => s.trim())
    .filter(Boolean);
}

function pairs(raw: string): { name: string; description: string }[] {
  return lines(raw).map((line) => {
    const idx = line.indexOf('::');
    if (idx === -1) return { name: line.trim(), description: '' };
    return {
      name: line.slice(0, idx).trim(),
      description: line.slice(idx + 2).trim(),
    };
  });
}

